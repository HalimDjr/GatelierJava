    private void brechercheMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_brechercheMouseClicked
        String s;
        s = cmchoix.getSelectedItem().toString();
        try {

            ps = co.getCo().prepareStatement("SELECT * FROM seances_dessayage WHERE " + s + "=?");
            ps.setInt(1, Integer.parseInt(trecherche.getText()));

            rs = ps.executeQuery();
            dtm.setRowCount(0);
            while (rs.next()) {
                dtm.addRow(new Object[]{rs.getInt("id"), rs.getDate("date"), rs.getInt("heure"), rs.getInt("id_commande")});

            }
            tabseance.setModel(dtm);
            if (dtm.getRowCount() != 0) {
                afficher(0);
            } else {
                actualiser();
                JOptionPane.showMessageDialog(this, "seance n'existe pas");
            }

        } catch (SQLException ex) {
            Logger.getLogger(SeancesE.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_brechercheMouseClicked

