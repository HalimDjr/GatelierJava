    public void actualiser() {
        //vider la table
        dtm.setColumnCount(0);
        dtm.setRowCount(0);
        try {
            stat = co.getCo().createStatement();
            rs = stat.executeQuery("select * from seances_dessayage");
            dtm.addColumn("numero");
            dtm.addColumn("date");
            dtm.addColumn("heure");
            dtm.addColumn("id_commande");

            while (rs.next()) {
                dtm.addRow(new Object[]{
                    rs.getInt("id"), rs.getDate("date"), rs.getInt("heure"), rs.getInt("id_commande")
                });
            }
            tabseance.setModel(dtm);
        } catch (SQLException ex) {
            Logger.getLogger(SeancesE.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void afficher(int nligne) {
        tdate1.setDate((Date) tabseance.getValueAt(nligne, 1));
        theure.setText(tabseance.getValueAt(nligne, 2).toString());
        idcommande.setText(tabseance.getValueAt(nligne, 3).toString());

        try {
            stat = co.getCo().createStatement();
            rs = stat.executeQuery("SELECT c.id,c.nom,c.prenom FROM clients c,commandes_sm cm WHERE c.id= cm.id_client and cm.id=" + Integer.parseInt(tabseance.getValueAt(nligne, 3).toString()));

            if (rs.next()) {
                // Si le ResultSet contient des lignes, récupérez les données
                idclient.setText(rs.getString("id"));
                nomclient.setText(rs.getString("nom"));
                prenomclient.setText(rs.getString("prenom"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(SeancesE.class.getName()).log(Level.SEVERE, null, ex);
        }
        idcommande.setVisible(true);

        idclient.setVisible(true);
        nomclient.setVisible(true);
        prenomclient.setVisible(true);

        lidc.setVisible(true);
        lidm.setVisible(true);
        lnm.setVisible(true);
        laddm.setVisible(true);
        bsuuprimer.setVisible(true);
        pmodifier.setVisible(true);

    }

