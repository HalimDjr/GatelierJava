    public void actualiser() {
        //vider la table
        dtm.setColumnCount(0);
        dtm.setRowCount(0);
        try {
            cv.ps = cv.co.getCo().prepareStatement("SELECT* FROM variantes");
            cv.rs = cv.ps.executeQuery();
        } catch (SQLException ex) {
            Logger.getLogger(ListeDesVariantes.class.getName()).log(Level.SEVERE, null, ex);
        }

        dtm.addColumn("id");
        dtm.addColumn("nom");
        dtm.addColumn("style");

        try {
            while (cv.rs.next()) {
                dtm.addRow(new Object[]{
                    cv.rs.getInt("id"), cv.rs.getString("nom"), cv.rs.getString("style")
                });
            }
            tabVariante.setModel(dtm);
        } catch (SQLException ex) {
            Logger.getLogger(ListeDesVariantes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

