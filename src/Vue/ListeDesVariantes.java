    private void bmodifyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bmodifyMouseClicked
        if (JOptionPane.showConfirmDialog(this, "voullez vous vraiment modifier ?", "modification",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            String nom = tnomvar1.getText();
            String style = tstyle1.getText();

            boolean vide = false;

            if (nom.equals("") || style.equals("")) {
                vide = true;
            }
            if (vide == false) {
                try {
                    ps = co.getCo().prepareStatement("UPDATE variantes SET nom=?,"
                            + "style=? WHERE id=?");
                    ps.setString(1, nom);
                    ps.setString(2, style);

                    ps.setInt(3, Integer.parseInt(tabVariante.getValueAt(tabVariante.getSelectedRow(), 0) + ""));
                    ps.execute();
                    JOptionPane.showMessageDialog(this, "Modification faite avec succes");
                    actualiser();
                    reinitialiser();
                } catch (SQLException ex) {
                    Logger.getLogger(Employes.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Vous devez remplir tous les champs");
            }
            pajoutervr.setVisible(false);
            jScrollPane1.setVisible(true);
            bmodifier.setVisible(false);
            bsupp.setVisible(false);
        }
    }//GEN-LAST:event_bmodifyMouseClicked

