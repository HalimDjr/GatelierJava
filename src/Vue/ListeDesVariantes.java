    private void bajouteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bajouteMouseClicked
        boolean vide = false;

        // recuperer les donnees du formulaire
        String nom = tnomvar1.getText();
        String style = tstyle1.getText();

        if (nom.equals("") || style.equals("")) {
            vide = true;
        }

        if (vide == false) {
            Pattern pattern = Pattern.compile("[^a-zA-Z]");
            Matcher matchernom = pattern.matcher(nom);
            Matcher matcherS = pattern.matcher(style);
            if (matchernom.find() || matcherS.find()) {

                JOptionPane.showMessageDialog(null, "la saisie n'est pas valide");
            } else {
                Variante vr = new Variante();
                vr.setNom(nom);
                vr.setStyle(style);

                ConnectVariante cvr = new ConnectVariante();
                boolean varianteExiste = false;
                try {
                    while (cvr.rs.next()) {
                        if (nom.equals(cvr.rs.getString("nom")) && style.equals(cvr.rs.getString("style"))) {
                            varianteExiste = true;
                            break;
                        }
                    }
                    if (!varianteExiste) {
                        stat = co.getCo().createStatement();
                        stat.executeUpdate("INSERT INTO variantes(nom,style) VALUES('"
                                + vr.getNom() + "','" + vr.getStyle() + "')");
                        JOptionPane.showMessageDialog(null, "variante ajouter avec succes");
                       
                        reinitialiser();
                         
                        pajoutervr.setVisible(false);
                        bmodifier.setVisible(false);
                        bsupp.setVisible(false);
                        jScrollPane1.setVisible(true);
                        tabVariante.setVisible(true);
                        actualiser();
                    } else {
                        JOptionPane.showMessageDialog(null, "La variante existe déjà");
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(Employes.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Vous devez remplir tous les champs");
        }

    }//GEN-LAST:event_bajouteMouseClicked

